# ── Config Loading ────────────────────────────────────────────────────────────

def load_existing_config():
    """Load existing config for defaults, if any."""
    config_file = Path.home() / ".openclaw" / "config.yaml"
    if not config_file.exists():
        return {}
    try:
        import yaml
        with open(config_file) as f:
            return yaml.safe_load(f) or {}
    except Exception:
        return {}


def write_config(config, config_file, is_local, cloud_model, model_name):
    """
    Write config to file with backup.

    Args:
        config: Config dict to write
        config_file: Path to config file
        is_local: Whether in local mode
        cloud_model: Cloud model name (if cloud mode)
        model_name: Local model name (if local mode)
    """
    import yaml

    if config_file.exists():
        backup = config_file.with_suffix(".yaml.bak")
        shutil.copy2(config_file, backup)
        ok(f"Backed up existing config to {backup.name}")

    mode_label = "LOCAL-ONLY MODE (Qwen via Ollama)" if is_local else "CLOUD MODE (Anthropic Claude)"
    with open(config_file, "w") as f:
        f.write(f"# Rout config — {mode_label}\n")
        f.write("# Generated by: python3 setup.py\n")
        f.write("# Edit freely. Re-run to reconfigure.\n\n")
        yaml.dump(config, f, default_flow_style=False, sort_keys=False, allow_unicode=True)

    os.chmod(config_file, 0o600)
    ok(f"Config saved to {config_file}")


# ── API Key Testing ───────────────────────────────────────────────────────────

def test_api_key(key):
    """
    Send a minimal request to verify the API key works.

    Returns:
        (success, error_msg) — (bool, str or None)
    """
    try:
        import requests
        resp = requests.post(
            "https://api.anthropic.com/v1/messages",
            headers={
                "x-api-key": key,
                "content-type": "application/json",
                "anthropic-version": "2023-06-01",
            },
            json={
                "model": "claude-sonnet-4-20250514",
                "max_tokens": 10,
                "messages": [{"role": "user", "content": "Say ok"}],
            },
            timeout=15,
        )
        if resp.status_code == 200:
            return True, None
        elif resp.status_code == 401:
            return False, "Invalid key (401 Unauthorized)"
        elif resp.status_code == 403:
            return False, "Key doesn't have API access (403 Forbidden)"
        else:
            return False, f"API returned {resp.status_code}: {resp.text[:100]}"
    except Exception as e:
        return False, f"Connection error: {e}"


