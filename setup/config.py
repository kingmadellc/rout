def install_ollama():
    """Install Ollama if not present."""
    if shutil.which("ollama"):
        return True

    print(f"\n  Installing Ollama...")
    brew_path = shutil.which("brew")
    if brew_path:
        result = subprocess.run(
            [brew_path, "install", "ollama"],
            capture_output=False, timeout=300,
        )
        if result.returncode == 0 and shutil.which("ollama"):
            ok("Ollama installed via Homebrew")
            return True

    # Fallback to curl installer
    try:
        result = subprocess.run(
            ["bash", "-c", "curl -fsSL https://ollama.com/install.sh | sh"],
            capture_output=True, text=True, timeout=300,
        )
        if shutil.which("ollama"):
            ok("Ollama installed")
            return True
    except Exception:
        pass

    fail("Could not install Ollama. Install manually: https://ollama.com/download")
    return False


def ensure_ollama_running():
    """Start Ollama server if not already running."""
    try:
        req = urllib.request.Request("http://localhost:11434/api/tags")
        urllib.request.urlopen(req, timeout=3)
        return True
    except Exception:
        pass

    print(f"  Starting Ollama server...")
    subprocess.Popen(
        ["ollama", "serve"],
        stdout=subprocess.DEVNULL,
        stderr=subprocess.DEVNULL,
    )
    # Wait for it to come up
    for _ in range(10):
        time.sleep(1)
        try:
            req = urllib.request.Request("http://localhost:11434/api/tags")
            urllib.request.urlopen(req, timeout=3)
            ok("Ollama server started")
            return True
        except Exception:
            continue

    fail("Ollama server did not start. Run manually: ollama serve")
    return False


def pull_model(model_name):
    """Pull an Ollama model. Shows progress."""
    print(f"\n  Pulling {model_name} — this may take 10-30 minutes on first run...")
    print(f"  {DIM}(Progress shown below){NC}\n")

    result = subprocess.run(
        ["ollama", "pull", model_name],
        capture_output=False,
        timeout=1800,  # 30 min timeout
    )

    if result.returncode == 0:
        ok(f"Model {model_name} ready")
        return True
    else:
        fail(f"Failed to pull {model_name}. Run manually: ollama pull {model_name}")
        return False


def test_local_inference(model_name):
    """Quick inference test to verify the model works."""
    print(f"  Testing inference...")
    try:
        body = json.dumps({
            "model": model_name,
            "messages": [{"role": "user", "content": "Say 'online' in one word."}],
            "stream": False,
            "options": {"num_predict": 10},
        }).encode("utf-8")
        req = urllib.request.Request(
            "http://localhost:11434/api/chat",
            data=body,
            headers={"Content-Type": "application/json"},
        )
        resp = urllib.request.urlopen(req, timeout=120)
        result = json.loads(resp.read())
        text = result.get("message", {}).get("content", "").strip()
        if text:
            ok(f"Inference test passed: \"{text}\"")
            return True
        else:
            warn("Model returned empty response — may still work in practice")
            return True
    except Exception as e:
        fail(f"Inference test failed: {e}")
        return False


def test_local_tool_calling(model_name):
    """Test that the model can handle function calling."""
    print(f"  Testing tool calling...")
    try:
        body = json.dumps({
            "model": model_name,
            "messages": [{"role": "user", "content": "What's the weather today? Use the search tool."}],
            "tools": [{
                "type": "function",
                "function": {
                    "name": "web_search",
                    "description": "Search the web for current information.",
                    "parameters": {
                        "type": "object",
                        "properties": {
                            "query": {"type": "string", "description": "Search query"},
                        },
                        "required": ["query"],
                    },
                },
            }],
            "stream": False,
            "options": {"num_predict": 100},
        }).encode("utf-8")
        req = urllib.request.Request(
            "http://localhost:11434/api/chat",
            data=body,
            headers={"Content-Type": "application/json"},
        )
        resp = urllib.request.urlopen(req, timeout=120)
        result = json.loads(resp.read())
        tool_calls = result.get("message", {}).get("tool_calls", [])
        if tool_calls:
            name = tool_calls[0].get("function", {}).get("name", "?")
            ok(f"Tool calling works — model called {name}()")
            return True
        else:
            warn("Model didn't use tool calling on test prompt — may work with real prompts")
            return True
    except Exception as e:
        warn(f"Tool calling test inconclusive: {e}")
        return True  # Non-fatal


def setup_local_provider(existing):
    """
    Configure local provider.

    Returns:
        (model_name, local_config) — (str, dict)
    """
    print(f"\n{BOLD}Local Model{NC}")
    print("-" * 30)

    ram_gb = get_system_ram_gb()
    model_options = get_local_model_options(ram_gb)

    if not model_options:
        fail(f"No models available for {ram_gb}GB RAM. Minimum 8GB required.")
        sys.exit(1)

    # Default to the largest model that fits
    default_idx = len(model_options)

    model_choice = ask_choice(
        f"Choose your model ({ram_gb}GB RAM detected):",
        model_options,
        default=default_idx,
    )

    model_name = model_choice["model"]
    gpu_mem_mb = model_choice["gpu_mem_mb"]
    ctx_length = model_choice["ctx"]

    ok(f"Selected: {model_name}")

    # Install Ollama if needed
    if not install_ollama():
        sys.exit(1)

    # Start Ollama
    if not ensure_ollama_running():
        sys.exit(1)

    # Set GPU memory cap
    if platform.system() == "Darwin":
        print(f"  Setting GPU memory cap to {gpu_mem_mb}MB...")
        try:
            subprocess.run(
                ["sudo", "sysctl", f"iogpu.wired_limit_mb={gpu_mem_mb}"],
                capture_output=True, timeout=10,
            )
            ok(f"GPU memory cap set to {gpu_mem_mb}MB")
        except Exception:
            warn("Could not set GPU memory cap (may need sudo). Continuing...")

    # Pull model
    if not pull_model(model_name):
        proceed = ask("Model pull failed. Continue anyway? (y/N)", default="n")
        if not proceed.lower().startswith("y"):
            sys.exit(1)

    # Test inference
    test_local_inference(model_name)
    test_local_tool_calling(model_name)

    # Auto-start
    if platform.system() == "Darwin":
        setup_ollama_autostart()

    local_config = {
        "enabled": True,
        "provider": "ollama",
        "host": "http://localhost:11434",
        "model": model_name,
        "timeout_seconds": 120,
        "max_tokens": 4096,
        "temperature": 0.7,
        "context_length": ctx_length,
        "gpu_memory_cap_mb": gpu_mem_mb,
    }

    return model_name, local_config
